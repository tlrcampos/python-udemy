"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos que estão dentro de aspas
"""
#Numérico
print(1234)

#Aspas duplas
print("Luiz Otávio")

#Aspas simples
print('Luiz')
print(1, '"Luiz Otávio"')

#Escape
print("Luiz \'Otávio\'")
print("\"Luiz\" Otávio")

#r
print(r"Luiz \"Otávio\"")
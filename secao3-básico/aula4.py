# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado
# posito.

print(11) #int
print(-11) #int
print(0) 

# float -> Número com ponto flutuante 
# O tipo float representatividade qualquer número
# positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo.

print(1.1)  #float
print(1.1, -10.32) #float
print(21.21, -11.43, sep=" | ") #float
print(0.0, -1.5) #float

# A função type mostra o tipo que o Python 
# inferiu ao valor.

print(type(4))
print(type(4.23))
print(type('Otávio'))
print(type(0.0), type(1.1), type(-32.12), sep=" | ")
